import { readFile, writeFile } from 'node:fs/promises';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { assets } from '../index.js';

const rootDirectory = dirname(fileURLToPath(new URL('../package.json', import.meta.url)));
const outputPath = join(rootDirectory, 'embedded.js');

function serializeJson(value) {
  return JSON.stringify(value, null, 2).replaceAll('</script', '<\\/script');
}

const embeddedAssets = [];

for (const asset of assets) {
  const content = await readFile(join(rootDirectory, asset.file));
  embeddedAssets.push({
    ...asset,
    contentBase64: content.toString('base64'),
  });
}

const output = [
  '// Generated by scripts/generate-embedded.mjs. Do not edit manually.',
  `export const embeddedAssets = ${serializeJson(embeddedAssets)};`,
  '',
  'const embeddedAssetsByKey = new Map(embeddedAssets.map((asset) => [asset.key, asset]));',
  '',
  'export function getEmbeddedAsset(key) {',
  '  const asset = embeddedAssetsByKey.get(key);',
  '  if (!asset) {',
  '    throw new Error(`Unknown GFM addon asset: ${key}`);',
  '  }',
  '  return asset;',
  '}',
  '',
  'export function getEmbeddedAssetContent(key, encoding = "utf8") {',
  '  const asset = getEmbeddedAsset(key);',
  '  const content = Buffer.from(asset.contentBase64, "base64");',
  '  return encoding === null ? content : content.toString(encoding);',
  '}',
  '',
].join('\n');

await writeFile(outputPath, output);
console.log(`generated ${outputPath}`);
